package mac

import "net"

// FormatRFC7042 formats a hardware address following the format in
// RFC 7042. Each octet is represented by two uppercase hexadecimal digits
// and successive octets are separated by a hyphen. For example
// 00-00-5E-00-53-00. This is the format used by ECS for MAC addresses.
func FormatRFC7042(a net.HardwareAddr) string {
	const hexDigit = "0123456789ABCDEF"

	if len(a) == 0 {
		return ""
	}
	buf := make([]byte, 0, len(a)*3-1)
	for i, b := range a {
		if i > 0 {
			buf = append(buf, '-')
		}
		buf = append(buf, hexDigit[b>>4])
		buf = append(buf, hexDigit[b&0xF])
	}
	return string(buf)
}
